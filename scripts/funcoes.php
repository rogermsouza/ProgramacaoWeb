<?php
    function circulo($num){
        $resultado = pow($num, 2);
        return($resultado);
    }
    function triangulo($base, $altura){
        $result = ($base * $altura) / 2;
        return($result);
    }
    function retangulo($baseRet, $alturaRet){
        $resultRet = $baseRet * $alturaRet;
        return($resultRet);
    }
?>